// WAP for finding the volume of the cylinder by taking radius and height as input.
#include<iostream>
using namespace std;
int main(){
    int r,h;
    cout<<"enter radius of cyliner: ";
    cin>>r;
    cout<<"enter height of cyliner: ";
    cin>>h;
    cout<<"the volume of cyliner will be: "<< 3.1415 * r * r * h <<endl;
    return 0;
}